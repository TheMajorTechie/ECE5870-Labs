/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
	//Configure the system clock 
	HAL_Init();
  SystemClock_Config();
	
	//enable GPIOC peripheral clock
	RCC->AHBENR |= RCC_AHBENR_GPIOAEN; 
	RCC->AHBENR |= RCC_AHBENR_GPIOCEN; 
	
	//enable timers 2 and 3 (page 125 in peripheral ref manual)
	RCC->APB1ENR |= RCC_APB1ENR_TIM2EN;
	RCC->APB1ENR |= RCC_APB1ENR_TIM3EN;
	
	//timer triggers an update event at 4Hz
			//default clock is 8MHz. we need to divide this down to 4Hz
			//write 1F3F to PSC
			//write FA to ARR
			//enable UEV interrupt on control regs
	TIM2->PSC = 7999;
	TIM2->ARR = 250;
	
	//set the UIE (Update Interrupt Enable) on TIM2 to 1
	TIM2->DIER |= (1);			

	//configure and enable/start the timer using its control regs
	TIM2->CR1 |= (1);
	
	//force the STM32 to use the 8MHz HSI clock using the SW switch on the clock multiplexor
	//RCC->CFGR &= (1); 
	
	//configure timer 3 to have an 800Hz period
	TIM3->PSC = 19;			//divide 8mhz clock by 20
	TIM3->ARR = 125;		//count to 125
	
	//configure CC1S[1:0] and CC2S[1:0] to set channels to output
	TIM3->CCMR1 &= 0xFFFC;			//~(3);				//clear first two bits
	TIM3->CCMR1 &= 0xFCFF;			//~(3 << 8);	//clear 8th and 9th bits
	
	//now set the OC1M[2:0] bit field to set channel 1 to PWM mode 2
	TIM3->CCMR1 |= (7 << 4);				//write 111 to OC1M
	//and OC2M[2:0] bit field set channel 2 to PWM mode 1
	TIM3->CCMR1 |= (6 << 12);				//write 110 to OC2M
	
	//enable output compare preload for both channels
	TIM3->CCMR1 |= (1 << 3); //TIM_CCMR1_OC1PE;
	TIM3->CCMR1 |= (1 << 11); //TIM_CCMR1_OC2PE;
	
	//set output enable bits for channels 1 and 2
	TIM3->CCER |= ((TIM_CCER_CC1E) | (TIM_CCER_CC2E)); //(1);
	//TIM3->CCER |= (1 << 4);
	
	//start timer 3
	TIM3->CR1 |= (1);
	
	//Set both channels' CCRx values to 20% of the ARR
	TIM3->CCR1 = 20;
	TIM3->CCR2 = 20;
	
	//configure LEDs
	GPIOC->MODER |= ((1 << 12) | (1 << 14) | (1 << 16) | (1 << 18));
	
	//clear the bits for output type to put into push-pull
	GPIOC->OTYPER &= ~((1 << 6) | (1 << 7) | (1 << 8) | (1 << 9));
	
	//clear the lower bits for the output speed register to put into low speed
	GPIOC->OSPEEDR &= ~((1 << 12) | (1 << 14) | (1 << 16) | (1 << 18));
	
	//clear both upper and lower bits to set no pull-ups or pull-downs
	GPIOC->PUPDR &= ~((1 << 12) | (1 << 13) | 
										(1 << 14) | (1 << 15) | 
										(1 << 16) | (1 << 17) |
										(1 << 18) | (1 << 19)
										);
										
	GPIOC->ODR &= ~(1 << 8);			//orange off
	GPIOC->ODR |= (1 << 9);		  //green on

//Blue and Red
GPIO_InitTypeDef redAndBlue = {GPIO_PIN_6 | GPIO_PIN_7,
			       GPIO_MODE_AF_PP,
			       GPIO_SPEED_FREQ_LOW,
			       GPIO_NOPULL 
};
HAL_GPIO_Init(GPIOC, &redAndBlue);
	
	
	//set up the timer's interrupt handler and enable in the NVIC
			//timer only has a single generic interrupt reserved in vector table
			//it is IRQ 15
	NVIC_EnableIRQ(15);	

	
	//TIM3->CCMR1 |= (3 << 4);		//configure toggle mode

	//configure the LEDs to alternate function mode
			//PC6 = TIM3_CH1
			//PC7 = TIM3_CH2
			//we want AF0 (0000)
	GPIOC->AFR[0] = 0;
	

  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

//toggle between green and orange LEDs
void TIM2_IRQHandler(void) {
	GPIOC->ODR ^= (1 << 8);
	GPIOC->ODR ^= (1 << 9);
	TIM2->SR &= ~(1);		//clear update interrupt (page 454)
}

